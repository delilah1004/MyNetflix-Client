package com.my.netflix.all.model;

public class Genre {

	// 장르 Id
	private int id;
	
	// 장르명
	private String name;

	public Genre() {
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
